"""
backend/api/sme_export_layouts.py — legacy SME workbook layouts (T3).

Byte-faithful port of the legacy Streamlit writers in
pages_internal/material_estimator_portal.py:
  - `_equipment_report_excel`   (lines ~2040-2406)  → equipment_report_xlsx()
  - `_location_report_excel`    (lines ~2426-2640)  → location_report_xlsx()
  - `generate_excel_report`     (lines ~1812+)      → single_table_xlsx()
  - `_standard_filename`        (lines ~105-114)    → legacy_filename()

Deliberately written with **xlsxwriter** (already a repo dependency — the
legacy portal uses it): the blueprint's format dicts, merge_range/insert_image
calls and row geometry translate 1:1, so there is no re-implementation drift.

Every sheet shares the legacy 5-row header:
  rows 0-3  logo (pages_internal/sme_logo.png) + right-aligned meta
            ("Report Generated:" timestamp · "Generated By:" username)
  row 4     merged title bar (scheme title_bg, white bold 13pt, height 22)
then the body:
  equipment report  → "1. Summary by Equipment (Total SQM)" ·
                      "2. Summary by System Code (Total SQM)" ·
                      "3. Detailed Table" (autofilter) — each with a
                      GRAND TOTAL row in the scheme's total colors
  location report   → main alloc table + GRAND TOTAL, then three summary
                      blocks (System Code · Equipment · Material)
  single table      → header row 5, data rows 6+, GRAND TOTAL last

Pure formatting helpers — no DB access, no writes (SME Canon intact).
"""
